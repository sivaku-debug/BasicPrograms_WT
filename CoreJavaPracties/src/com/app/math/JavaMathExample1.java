package com.app.math;

public class JavaMathExample1 {
static int a=10;
static  int b=2;
	public static void main(String[] args) {
		System.out.println("max value is:"+Math.max(a, b));
		System.out.println("min value is:"+Math.min(a, b));
		System.out.println("Square root value is:"+Math.sqrt(a));
		System.out.println("a Power of b is:"+Math.pow(a, b));
		System.out.println("Logorithm of a is:"+Math.log(a));
	}

}
